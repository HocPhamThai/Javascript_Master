// Assignment Operators: Toán tử gán

let a = 10; // Gán giá trị 10 cho biến a
console.log("Initial value of a:", a);

a += 5; // Tương đương a = a + 5
console.log("After a += 5:", a); // 15

a -= 3; // Tương đương a = a - 3
console.log("After a -= 3:", a); // 12

a *= 2; // Tương đương a = a * 2
console.log("After a *= 2:", a); // 24

a /= 4; // Tương đương a = a / 4
console.log("After a /= 4:", a); // 6

a %= 3; // Tương đương a = a % 4
console.log("After a %= 3:", a); // 0
