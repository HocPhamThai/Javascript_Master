// Arithmetic Operators: Toán tử số học

let x = 10;
let y = 5;

console.log("Addition (x + y):", x + y); // Cộng
console.log("Subtraction (x - y):", x - y); // Trừ
console.log("Multiplication (x * y):", x * y); // Nhân
console.log("Division (x / y):", x / y); // Chia
console.log("Modulus (x % y):", x % y); // Phần dư
console.log("Exponentiation (x ** y):", x ** y); // Lũy thừa

console.log("Increment (x++):", x++); // Tăng
console.log("Value of x after increment:", x); // Giá trị của x sau khi tăng
console.log("Decrement (y--):", y--); // Giảm
console.log("Value of y after decrement:", y); // Giá trị của y sau khi giảm
