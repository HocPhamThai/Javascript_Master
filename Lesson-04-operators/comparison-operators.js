// Comparison Operators: Toán tử so sánh

let x = 10;
let y = 5;

console.log(x == y); // Kiểm tra bằng: -----------------------false
console.log(x === y); // Kiểm tra bằng nghiêm ngặt: ----------false
console.log(x != y); // Kiểm tra không bằng: -----------------true
console.log(x !== y); // Kiểm tra không bằng nghiêm ngặt: ----true
console.log(x > y); // Kiểm tra lớn hơn: ---------------------true
console.log(x < y); // Kiểm tra nhỏ hơn: ---------------------false
console.log(x >= y); // Kiểm tra lớn hơn hoặc bằng: ----------true
console.log(x <= y); // Kiểm tra nhỏ hơn hoặc bằng: ----------false
